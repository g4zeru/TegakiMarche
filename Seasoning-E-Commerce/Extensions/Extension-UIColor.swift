import UIKit

extension UIColor {
    static var customBlack: UIColor {
        return UIColor(displayP3Red: 75/255, green: 75/255, blue: 75/255, alpha: 1)
    }
    
    static var customGray: UIColor {
        return UIColor(displayP3Red: 112/255, green: 112/255, blue: 112/255, alpha: 1)
    }
}
